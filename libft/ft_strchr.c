#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	int i;

	i = 0;
	if (!str)
		return (NULL);
	if (ch == '\0')
		return ((char *)str + ft_strlen(str));
	while (str[i])
	{
		if (str[i] == ch)
			return ((char *)str + i);
		i++;
	}
	return (NULL);
}
