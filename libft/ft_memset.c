#include "libft.h"

void	*ft_memset(void *dest, int val, size_t len)
{
	unsigned char *ptr;

	if (!dest)
		return (NULL);
	ptr = dest;
	while (len && ptr)
	{
		*ptr = val;
		ptr++;
		len--;
	}
	return (dest);
}
